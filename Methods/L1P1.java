/*
Write a program that has a method called printHello.  
The printHello method is public static void and takes no parameters.  
The main method should call the printHello method three times.  
The printHello method should print the word “hello.”
*/
package Methods;
public class L1P1 {
    public static void printHello() {
        System.out.println("hello.");
    }
    public static void main(String[] args) {
        printHello();
        printHello();
        printHello();
    }
}
